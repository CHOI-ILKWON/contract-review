// ─────────────────────────────────────────────────────────────
// 계약 유형별 추가 지시
// ─────────────────────────────────────────────────────────────
export function getContractTypeContext(contractType: string): string {
  const typeMap: Record<string, string> = {
    EPC: `
[계약 유형: EPC (설계·조달·시공)]
- 설계(Engineering), 조달(Procurement), 시공(Construction) 전 범위가 당사 책임인지 확인
- 준공 후 O&M 책임이 EPC 시공사에 전가되는 조항이 있으면 🔴 KEY RISK
- LD Cap 존재 여부 필수 확인 (없으면 ❌ 없음)
- 성능보증 위약금과 LD가 중복 적용되는 구조인지 확인
`,
    OM: `
[계약 유형: O&M (운영·유지보수)]
- 운영 범위와 EPC 하자보수 범위가 혼재되어 있으면 🔴 KEY RISK
- 운영 KPI 미달 시 위약금이 계약금액을 초과하는 구조인지 확인 → 초과 시 🔴 KEY RISK
- 운영 중 발생하는 설비 노후화 비용을 O&M 사업자가 부담하는 구조인지 확인
- 운영 종료 후 자산 반환 조건 확인
`,
    SUBCONTRACT: `
[계약 유형: 하도급(Subcontract)]
- 원도급 계약 조건이 Back-to-Back으로 전가되는지 확인 (전가 범위가 불명확하면 🔴 KEY RISK)
- 원도급자(Prime Contractor) 귀책으로 발생한 LD를 하수급인이 부담하는 구조인지 확인 → 🔴 KEY RISK
- 원도급 계약상 연대책임이 하도급으로 흘러내려오는 구조인지 확인
- 하도급 LD Cap이 하도급 계약금액 이내인지 확인
`,
    LOI: `
[계약 유형: LOI·MOU·확약서]
- 법적 구속력(Binding) 조항이 명확한지 확인 (모호하면 🔴 KEY RISK)
- 본계약 체결 기한 명시 여부 확인 (없으면 ❌ 없음)
- 파기 시 비용 처리 조항 확인 (없으면 ❌ 없음)
- 선투입 비용(설계비, 조달비 등) 보전 조항 확인
`,
    OTHER: `
[계약 유형: 기타]
- 계약 유형을 먼저 판별하고, 해당 유형에 맞는 필수 항목을 체크한다.
- 당사 절대원칙 5가지를 기준으로 계약 전체를 검토한다.
`,
  };
  return typeMap[contractType] ?? typeMap.OTHER;
}
